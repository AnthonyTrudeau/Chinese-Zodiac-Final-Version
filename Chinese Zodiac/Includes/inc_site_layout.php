<h1>Site Layout.</h1>
<p>
A Dynamic Web Template is a HTML based copy of a web page that is created
to contain everything you need for settings and different page elements like 
graphics, page layout, styles, and modible areas. 
</p>
<hr />
<h1>Why Dynamic?</h1>
<p>
Dynamic Web sites are easier to modify becayse you can attatch them to a 
database that you can pull your information out of. Where as a Static Web 
site alows you to theme your websites easier, where a Dynamic usually only 
has one theme. Static how ever you usually have to go back to the designer 
of the page in order to add content, where in Dynamic you just add to the 
database when you need to add items.
</p>
<hr />
<h1>How to create a Dynamic Web Template.</h1>
<p>
To create a Dynamic Web Template, you need to start with an new file saving 
it as index.php. Once you have the index file made which will be your main page 
you need to add includes for any of the parts of the site you wish to have, 
including things like a header, body, aditional content, footer, and anything 
else you might need. Then you go and make these include files each seperatly 
to do what you want them to do, making the footer say what ypou want it to, 
making your main page content, and everything else you want(including buttons 
to move between the content ifyou want more than a small page). Once that is 
done you can start working on how the content looks, either as in line style, 
which is harder to change when you have to hunt it down, or using an internal 
or external style sheet on the index page.
</p>
